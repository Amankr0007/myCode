//Write a C program to check whether a given number is even or odd
package If_Else;
import java.util.*;
public class E10 {
	public static void main(String args[])
	{
		Scanner sc = new Scanner(System.in);
		int n=sc.nextInt();
		if(n%2==0)
		{
			System.out.println("even");
		}
		else
		{
			System.out.println("odd");
			
		}
	}

}
